// ─── Computed statistics ───

export function computeStats(cases: HantavirusCase[], year: number | 'all'): StatsOverview {
  const filtered = year === 'all' ? cases : cases.filter(c => c.year === year);
  const totalCases = filtered.reduce((sum, c) => sum + c.confirmedCases, 0);
  const totalDeaths = filtered.reduce((sum, c) => sum + c.deaths, 0);
  const countries = new Set(filtered.map(c => c.country));

  return {
    totalCases,
    totalDeaths,
    caseFatalityRate: totalCases > 0 ? parseFloat(((totalDeaths / totalCases) * 100).toFixed(1)) : 0,
    countriesAffected: countries.size,
    lastUpdated: '2024-05-01T00:00:00Z',
    trendCases: 8.3,
    trendDeaths: -2.1,
  };
}

