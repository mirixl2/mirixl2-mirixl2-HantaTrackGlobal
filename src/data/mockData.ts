// ─── CSV export utility ───

export function exportCasesToCSV(cases: HantavirusCase[]): string {
  const headers = ['País', 'Código', 'Región', 'Latitud', 'Longitud', 'Casos Confirmados', 'Fallecidos', 'Tasa Letalidad (%)', 'Cepa', 'Fuente', 'Última Actualización', 'Año'];
  const rows = cases.map(c => [
    c.country,
    c.countryCode,
    c.region,
    c.lat,
    c.lng,
    c.confirmedCases,
    c.deaths,
    c.caseFatalityRate,
    c.strain,
    c.source,
    c.lastUpdated,
    c.year,
  ].join(','));

  return [headers.join(','), ...rows].join('\n');
}

