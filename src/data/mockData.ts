// ─── Country ranking (computed from case data) ───

export function computeCountryRankings(cases: HantavirusCase[]): CountryRanking[] {
  const countryMap = new Map<string, CountryRanking>();

  for (const c of cases) {
    const existing = countryMap.get(c.country);
    if (existing) {
      existing.totalCases += c.confirmedCases;
      existing.totalDeaths += c.deaths;
    } else {
      countryMap.set(c.country, {
        country: c.country,
        countryCode: c.countryCode,
        totalCases: c.confirmedCases,
        totalDeaths: c.deaths,
        caseFatalityRate: 0,
        region: c.region,
      });
    }
  }

  const rankings = Array.from(countryMap.values());
  for (const r of rankings) {
    r.caseFatalityRate = r.totalCases > 0
      ? parseFloat(((r.totalDeaths / r.totalCases) * 100).toFixed(1))
      : 0;
  }

  return rankings.sort((a, b) => b.totalCases - a.totalCases);
}

